package com.Elephants.doctolibElephants.model;

public class FollowUpStatus {
    private Integer green;
    private Integer orange;
    private Integer red;
    private Integer total;
    private Integer totalPris;
    private Integer restePrendre;
    public FollowUpStatus() {
    }
    public Integer getGreen() {
        return green;
    }
    public void setGreen(Integer green) {
        this.green = green;
    }
    public Integer getOrange() {
        return orange;
    }
    public void setOrange(Integer orange) {
        this.orange = orange;
    }
    public Integer getRed() {
        return red;
    }
    public void setRed(Integer red) {
        this.red = red;
    }
    public Integer getTotal() {
        return total;
    }
    public void setTotal(Integer total) {
        this.total = total;
    }
    public Integer getTotalPris() {
        return totalPris;
    }
    public void setTotalPris(Integer totalPris) {
        this.totalPris = totalPris;
    }
    public Integer getRestePrendre() {
        return restePrendre;
    }
    public void setRestePrendre(Integer restePrendre) {
        this.restePrendre = restePrendre;
    }
}